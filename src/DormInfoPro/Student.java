package DormInfoPro;

public class Student {
    public String num;
    public String name;
    public String sex;
    public String dormId;
    public String institute;
    public String classname;

    public Student(String num,String name,String sex,String dormId,String institute,String classname)
    {
        this.num=num;
        this.name=name;
        this.sex=sex;
        this.dormId=dormId;
        this.institute=institute;
        this.classname=classname;
    }
}
